package packStar123;

public class Star6 {
	
public static void main(String[] args) {
		
		for (int a=1; a<=5; a++)
		{
		  for(int b=1; b<=a; b++)
		  {
			System.out.print("*");
		  }
			System.out.println();
		  }
		  for(int a = 1; a<=5; a++)
		  {
		   for(int c=4; c>=a;c--)
		  {
			System.out.print("*");
		  }
			System.out.println();
		  }
    }
}

