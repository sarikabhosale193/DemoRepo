package Loop;

public class While {

	public static void main(String[] args) {
		
		int a = 1;
		while(a<5)
		{
		  System.out.println("Hi");
		  a++;
		}
		System.out.println("___________________");
		
		int b =5;
		while(b>1)
		{
			System.out.println("Hello");
			b--;
		}
		System.out.println("__________________");
		
	}
}
        