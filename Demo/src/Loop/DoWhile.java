package Loop;

public class DoWhile {
	
	public static void main(String[] args) {
		
		int a = 1;
		do
		{
			System.out.println("Hi");
			a++;
		}
		while(a<=6);
		System.out.println("_______________");
		
		int b=5;
		do
		{
			System.out.println("Hello");
			b--;
		}while(b>1);
	}
}
